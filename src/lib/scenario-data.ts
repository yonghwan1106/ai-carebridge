// Calculate total duration for auto-play mode
export const getTotalDuration = (): number => {
  return scenarioMessages.reduce((total, msg) => total + (msg.delay || 0), 0);
};

// Get message by index with cumulative time
export const getMessageWithTime = (index: number): { message: Message; cumulativeTime: number } | null => {
  if (index < 0 || index >= scenarioMessages.length) return null;

  let cumulativeTime = 0;
  for (let i = 0; i <= index; i++) {
    cumulativeTime += scenarioMessages[i].delay || 0;
  }

  return {
    message: scenarioMessages[index],
    cumulativeTime,
  };
};
